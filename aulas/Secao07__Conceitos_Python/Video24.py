# video 24 - Nomenclatura

# Nomenclatura - caracteres
"""
Evitar utilizar os caracteres sozinhos: I e O
Devido ao tipo de fonte utilizado pode atrapalhar identificar
se é número ou letras.
"""

# Nomenclatura - Pacotes e Módulos
"""
utilizar nomes pequenos se possível minusculo
exemplo: os
exenplo: package
"""

# Nomenclatura das classes
"""
Iniciar com letras maiusculas
exemplo: NomeDaClasse
"""

# Funções e métodos(módulos)
"""
Iniciar com letras minusculas
Quando tiver nomes compostos utilizar "underscore" (_)
exemplo: utilizar_underscore()
exemplo: enviar_email()
"""

# Nome(Nomenclatura) para as constantes
"""
Utilizar somente maiusculas
exemplo: VALOR_MAXIMO
exemplo: PI
"""

# Nome(Nomenclatura) ara parâmetros de funções
"""
letras minusculas
Nomes compostos unidos por underscore (_)
exemplo: enviar(nome_do_arquivo)
exemplo: receber(self)
"""
