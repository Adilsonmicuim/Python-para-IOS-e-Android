num1 = input("Digite um número: ")
num1 = int(num1)

num2 = input("Digite segundo número: ")
num2 = int(num2)

if (num1 == num2):  # Operador de igualdade
    print("O número %d é igual a %d " % (num1, num2))

if (num1 != num2):  # Operador de diferença
    print("O número %d é diferente de %d " % (num1, num2))

if (num1 <= num2):  # Operador menor que
    print("O número %d é menor que %d " % (num1, num2))

if (num1 >= num2):  # Operador maior que
    print("O número %d é maior que %d " % (num1, num2))
